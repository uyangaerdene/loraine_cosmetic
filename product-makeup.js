  function createProductElement(product) {
    const article = document.createElement("article");
    article.className = `prod-box ${product.class}`;
    article.setAttribute("data-brand", product.brand);
  
    const anchor = document.createElement("a");
    anchor.href = "product_detail.html";
  
    const img = document.createElement("img");
    img.src = product.imgSrc;
    img.alt = "";
  
    anchor.appendChild(img);
  
    const h4 = document.createElement("h4");
    h4.textContent = product.name;
  
    const detailP = document.createElement("p");
    detailP.classList.add("prod-detail");
    detailP.textContent = product.type;
  
    const priceP = document.createElement("p");
    priceP.classList.add("prod-price");
    priceP.textContent = product.price;
  
    const button = document.createElement("button");
    button.classList.add("add-cart");
    button.textContent = "Сагсанд нэмэх";
  
    article.appendChild(anchor); 
    article.appendChild(h4);
    article.appendChild(detailP);
    article.appendChild(priceP);
    article.appendChild(button);
  
    return article;
  }
  
  
  function populateProductList() {
    const section = document.querySelector(".product-list");
  
    const uniqueProducts = new Set();
  
    products.forEach((product) => {
      const productString = JSON.stringify(product);
    
      if (!uniqueProducts.has(productString)) {
        const productElement = createProductElement(product);
        section.appendChild(productElement);
        uniqueProducts.add(productString);
      }
    });
  }
  
  populateProductList();

  